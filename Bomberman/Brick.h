#pragma once
////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include "GameObject.h"
#include "StaticObject.h"

////////////////////////////////////////////////////////////
/// \brief The game bricks
///
////////////////////////////////////////////////////////////
class Brick : public StaticObject {
public:
	////////////////////////////////////////////////////////////
	/// \brief Constructor
	///
	/// \param texture   Source texture
	/// \param rectangle Sub-rectangle of the texture to assign to the sprite
	/// \param position  The object position 
	///
	////////////////////////////////////////////////////////////
	explicit Brick(const sf::Texture& texture, const sf::IntRect& rectangle, const sf::Vector2f& position);

	////////////////////////////////////////////////////////////
	/// \brief Destructor
	///
	////////////////////////////////////////////////////////////
	virtual ~Brick() = default;

	////////////////////////////////////////////////////////////
	/// \brief Plays the object animation 
	///
	/// \param direction - the key of the direction
	///
	////////////////////////////////////////////////////////////
	void playAnimation(const int direction) override;
};


////////////////////////////////////////////////////////////
/// \brief The game hard bricks
///
////////////////////////////////////////////////////////////
class HardBrick : public Brick {
public:
	////////////////////////////////////////////////////////////
	/// \brief Constructor
	///
	/// Sets the brick texture size and position
	///
	////////////////////////////////////////////////////////////
	explicit HardBrick(const sf::Vector2f& position);

	////////////////////////////////////////////////////////////
	/// \brief Check if object expired
	///
	////////////////////////////////////////////////////////////
	bool isExpired() const override;
};


////////////////////////////////////////////////////////////
/// \brief The game soft bricks
///
////////////////////////////////////////////////////////////
class SoftBrick : public Brick {
public:
	////////////////////////////////////////////////////////////
	/// \brief Constructor
	///
	/// Sets the brick texture size and position
	///
	////////////////////////////////////////////////////////////
	explicit SoftBrick(const sf::Vector2f& position);

	////////////////////////////////////////////////////////////
	/// \brief Check if object expired
	///
	////////////////////////////////////////////////////////////
	bool isExpired() const override;
};

